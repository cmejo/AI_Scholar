    def _get_status_class(self, value: float, threshold: float) -> str:
        """Get CSS class based on value and threshold"""
        if value >= threshold:
            return 'status-excellent'
        elif value >= threshold * 0.8:
            return 'status-good'
        elif value >= threshold * 0.6:
            return 'status-warning'
        else:
            return 'status-error'


