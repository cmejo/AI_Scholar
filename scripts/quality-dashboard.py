        alerts_html = ""
        for alert in alerts:
            alert_class = f"alert-{alert.get('severity', 'info')}"
            icon = "âŒ" if alert.get('severity') == 'error' else "âš ï¸" if alert.get('severity') == 'warning' else "â„¹ï¸"
            
