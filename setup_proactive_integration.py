def create_startup_scripts():
    """Create startup scripts for easy development"""
    print_step(4, "Startup Scripts Creation")
    
    # Create start_celery.sh script
    celery_script = """#!/bin/bash
# Start Celery workers and beat scheduler for AI Scholar

echo "Starting AI Scholar Celery services..."

# Check if Redis is running
if ! pgrep -x "redis-server" > /dev/null; then
    echo "Starting Redis server..."
    redis-server --daemonize yes
fi

# Start Celery worker in background
echo "Starting Celery worker..."
celery -A tasks.sync_tasks worker --loglevel=info --detach

# Start Celery beat scheduler in background
echo "Starting Celery beat scheduler..."
celery -A tasks.sync_tasks beat --loglevel=info --detach

# Optional: Start Flower monitoring
if command -v flower &> /dev/null; then
    echo "Starting Celery Flower monitoring..."
    celery -A tasks.sync_tasks flower --detach
    echo "Flower monitoring available at: http://localhost:5555"
fi

echo "✅ Celery services started successfully!"
echo "To stop services, run: pkill -f celery"
"""
    
    with open('start_celery.sh', 'w') as f:
        f.write(celery_script)
    
    os.chmod('start_celery.sh', 0o755)
    
    # Create stop_celery.sh script
    stop_script = """#!/bin/bash
# Stop Celery workers and beat scheduler

echo "Stopping AI Scholar Celery services..."

# Stop Celery processes
pkill -f "celery.*worker"
pkill -f "celery.*beat"
pkill -f "celery.*flower"

echo "✅ Celery services stopped!"
"""
    
    with open('stop_celery.sh', 'w') as f:
        f.write(stop_script)
    
    os.chmod('stop_celery.sh', 'w') 
    
    print("✅ Created startup scripts:")
    print("• start_celery.sh - Start all Celery services")
    print("• stop_celery.sh - Stop all Celery services")

