def print_step(step_num, title):
    """Print step header"""
    print(f"\n🔧 Step {step_num}: {title}")
    print("-" * 40)

def get_user_input(prompt, default=None, required=True):
    """Get user input with optional default"""
    if default:
        full_prompt = f"{prompt} [{default}]: "
    else:
        full_prompt = f"{prompt}: "
    
    while True:
        value = input(full_prompt).strip()
        
        if value:
            return value
        elif default:
            return default
        elif not required:
            return ""
        else:
            print("This field is required. Please enter a value.")

def create_env_file():
    """Create or update .env file with OAuth credentials"""
    print_step(1, "OAuth Credentials Configuration")
    
    print("To enable automated knowledge syncing, you need to configure OAuth credentials")
    print("for the platforms you want to connect. You can skip any platform you don't need.")
    print("\nFor setup instructions, visit:")
    print("• Google Drive: https://developers.google.com/drive/api/quickstart/python")
    print("• Notion: https://developers.notion.com/docs/authorization")
    print("• GitHub: https://docs.github.com/en/apps/oauth-apps/building-oauth-apps")
    print("• Dropbox: https://developers.dropbox.com/oauth-guide")
    
    # Read existing .env file if it exists
    env_file = Path('.env')
    existing_vars = {}
    
    if env_file.exists():
        print(f"\n📁 Found existing .env file")
        with open(env_file, 'r') as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith('#') and '=' in line:
                    key, value = line.split('=', 1)
                    existing_vars[key] = value
    
    # OAuth credentials to configure
    oauth_configs = {
        'Google Drive': {
            'client_id': 'GOOGLE_CLIENT_ID',
            'client_secret': 'GOOGLE_CLIENT_SECRET'
        },
        'Notion': {
            'client_id': 'NOTION_CLIENT_ID',
            'client_secret': 'NOTION_CLIENT_SECRET'
        },
        'GitHub': {
            'client_id': 'GITHUB_CLIENT_ID',
            'client_secret': 'GITHUB_CLIENT_SECRET'
        },
        'Dropbox': {
            'client_id': 'DROPBOX_CLIENT_ID',
            'client_secret': 'DROPBOX_CLIENT_SECRET'
        }
    }
    
    new_vars = existing_vars.copy()
    
    for platform, vars_config in oauth_configs.items():
        print(f"\n🔗 {platform} Configuration")
        
        configure = get_user_input(
            f"Do you want to configure {platform}? (y/n)", 
            "n", 
            required=False
        ).lower() == 'y'
        
        if configure:
            for var_name, env_var in vars_config.items():
                current_value = existing_vars.get(env_var, '')
                if current_value:
                    print(f"Current {var_name}: {current_value[:10]}...")
                
                new_value = get_user_input(
                    f"Enter {platform} {var_name.replace('_', ' ').title()}",
                    current_value if current_value else None,
                    required=False
                )
                
                if new_value:
                    new_vars[env_var] = new_value
    
    # Celery configuration
    print(f"\n⚙️ Celery Configuration")
    
    celery_vars = {
        'CELERY_BROKER_URL': 'redis://localhost:6379/0',
        'CELERY_RESULT_BACKEND': 'redis://localhost:6379/0'
    }
    
    for var_name, default_value in celery_vars.items():
        current_value = existing_vars.get(var_name, default_value)
        new_value = get_user_input(
            f"Enter {var_name}",
            current_value,
            required=False
        )
        
        if new_value:
            new_vars[var_name] = new_value
    
    # Write updated .env file
    with open(env_file, 'w') as f:
        f.write("# AI Scholar Environment Configuration\n")
        f.write("# Generated by setup_proactive_integration.py\n\n")
        
        f.write("# Database Configuration\n")
        if 'DATABASE_URL' in new_vars:
            f.write(f"DATABASE_URL={new_vars['DATABASE_URL']}\n")
        f.write("\n")
        
        f.write("# OAuth Credentials for Data Source Integration\n")
        for platform, vars_config in oauth_configs.items():
            f.write(f"# {platform}\n")
            for var_name, env_var in vars_config.items():
                if env_var in new_vars:
                    f.write(f"{env_var}={new_vars[env_var]}\n")
            f.write("\n")
        
        f.write("# Celery Configuration\n")
        for var_name in celery_vars:
            if var_name in new_vars:
                f.write(f"{var_name}={new_vars[var_name]}\n")
        f.write("\n")
        
        # Add any other existing variables
        other_vars = {k: v for k, v in new_vars.items() 
                     if k not in [env_var for config in oauth_configs.values() for env_var in config.values()] 
                     and k not in celery_vars 
                     and k != 'DATABASE_URL'}
        
        if other_vars:
            f.write("# Other Configuration\n")
            for var_name, value in other_vars.items():
                f.write(f"{var_name}={value}\n")
    
    print(f"\n✅ Environment configuration saved to {env_file}")

